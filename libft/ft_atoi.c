#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t	i;
	int		sign;
	int		num;

	sign = 1;
	num = 0;
	i = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
		|| str[i] == '\v' || str[i] == '\r' || str[i] == '\f')
		i++;
	if (str[i] == 45)
	{
		sign = -1;
		i++;
	}
	else if (str[i] == 43)
		i++;
	while (ft_isdigit(str[i]))
	{
		num = num * 10 + str[i++] - 48;
	}
	return (num * sign);
}
